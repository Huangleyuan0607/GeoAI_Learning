# 算术运算符： + - * / // % **
print("10 + 4 =",10 + 4)    # 加
print("10 - 4 =",10 - 4)    # 减
print("10 * 4 =",10 * 4)    # 乘
print("10 / 4 =",10 / 4)    # 除
print("10 // 4 =",10 // 4)  # 整除
print("10 % 4 =",10 % 4)    # 取余
print("10 ** 4 =",10 ** 4)  # 幂指数

# 算术运算符的优先级 --> ** --> * / // % --> + -
print("0.1 + 10 / 4**2 =",0.1 + 10 / 4**2)

# 案例：要求输入两个数x与y，分别输出x+y的结果和x-y的结果
# float() ---> 将其他类型转为浮点数类型
x = float(input("请输入x的值："))
y = float(input("请输入y的值："))

# 当计算0.5 - 0.4得到0.09999999999999998 --> 精度损失；由于计算机底层是基于二进制进行数据的存储与处理，二进制无法准确表示所有小数，因此涉及到浮点数的运算时可能损失精度
print(f"{x} + {y} = {x + y}")
print(f"{x} - {y} = {x - y}")
