# 逻辑运算符：与(and) 或(or) 非(not)
# 需求1：键盘输入一个整数，判断这个数字是否在10-20之间。
num = int(input("请输入一个整数："))
print(f"10 <= {num} <= 20 是否成立：",num >= 10 and num <= 20)   # and连接的条件是并且的关系，两条件同时True结果才是True，否则为False

# 需求2：键盘输入一个整数，判断这个数字是否不在10-20之间。
num1 = int(input("请输入一个整数："))
print(f"10 <= {num1} <= 20 是否不成立：",num1 <= 10 or num1 >= 20)    # or连接的条件是或者的关系，只要其中有一个成立结果就是True，全部不成立结果才是False










