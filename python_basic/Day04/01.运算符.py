# 赋值运算符：= += -+ *= /= %= //= **=
num = 85

num += 10
print("num += 10后，num =",num)   #num = 85 + 10 = 95

num -= 10
print("num -= 10后，num =",num)   # num = 95 - 10 = 85

num *= 10
print("num *= 10后，num =",num)   # num = 85 * 10 = 850

num /= 10
print("num /= 10后，num =",num)   # num = 850 / 10 = 85.0（除法运算的结果为小数）

num //= 10
print("num //= 10后，num =",num)  # num = 85.0 // 10 = 8.0（有浮点数参与到运算中，故结果为小数）

num %= 3
print("num %= 10后，num =",num)   # num = 8.0 % 3 = 2.0（有浮点数参与到运算中，故结果为小数）

num **= 3
print("num **= 10后，num =",num)  # num = 2.0 ** 3 = 8.0（有浮点数参与到运算中，故结果为小数）
